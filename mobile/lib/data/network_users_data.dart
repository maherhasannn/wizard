  static List<NetworkUser> getAllUsers() {
    return users;
  }

  static List<NetworkUser> getOnlineUsers() {
    return users.where((user) => user.isOnline).toList();
  }

  static List<NetworkUser> getUsersByCity(String city) {
    return users.where((user) => user.city.toLowerCase() == city.toLowerCase()).toList();
  }

  static List<NetworkUser> getUsersByInterests(List<String> interests) {
    return users.where((user) {
      return interests.any((interest) => 
        user.interests.any((userInterest) => 
          userInterest.toLowerCase().contains(interest.toLowerCase())
        )
      );
    }).toList();
  }

  static List<NetworkUser> searchUsers(String query) {
    if (query.isEmpty) return users;
    
    final lowercaseQuery = query.toLowerCase();
    return users.where((user) {
      return user.name.toLowerCase().contains(lowercaseQuery) ||
             user.city.toLowerCase().contains(lowercaseQuery) ||
             user.country.toLowerCase().contains(lowercaseQuery) ||
             user.bio.toLowerCase().contains(lowercaseQuery) ||
             user.instagram.toLowerCase().contains(lowercaseQuery);
    }).toList();
  }

  static NetworkUser? getUserById(String id) {
    try {
      return users.firstWhere((user) => user.id == id);
    } catch (e) {
      return null;
    }
  }
